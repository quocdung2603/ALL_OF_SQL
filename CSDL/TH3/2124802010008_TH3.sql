CREATE DATABASE QLMH
GO

USE QLMH
GO

CREATE TABLE KHACHHANG
(
	MAKH CHAR(4) PRIMARY KEY,
	HOTEN VARCHAR(40),
	DCHI VARCHAR(50),
	SODT VARCHAR(20),
	NGSINH SMALLDATETIME,
	NGDK SMALLDATETIME,
	DOANHSO MONEY,
)

CREATE TABLE NHANVIEN
(
	MANV CHAR(4) PRIMARY KEY,
	HOTEN VARCHAR(40),
	SODT VARCHAR(20),
	NGVL SMALLDATETIME,
)

CREATE TABLE SANPHAM
(
	MASP CHAR(4) PRIMARY KEY,
	TENSP VARCHAR(40),
	DVT VARCHAR(20),
	NUOCSX VARCHAR(40),
	GIA MONEY,
)

CREATE TABLE HOADON
(
	SOHD INT PRIMARY KEY,
	NGHD SMALLDATETIME ,
	MAKH CHAR(4) FOREIGN KEY(MAKH) REFERENCES KHACHHANG(MAKH),
	MANV CHAR(4) FOREIGN KEY(MANV) REFERENCES NHANVIEN(MANV),
	TRIGIA MONEY , 
)

CREATE TABLE CTHD
(
	SOHD INT FOREIGN KEY (SOHD) REFERENCES HOADON(SOHD),
	MASP CHAR(4) FOREIGN KEY(MASP) REFERENCES SANPHAM(MASP),
	SL INT,
	PRIMARY KEY(SOHD, MASP),
)

